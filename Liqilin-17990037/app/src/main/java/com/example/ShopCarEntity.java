package com.example;


public class ShopCarEntity {
    public String name;
    public int img;
    public String jieshao;
    public String price;
    public int number;

    public ShopCarEntity(String name, int img, String jieshao, String price, int number) {
        this.name = name;
        this.img = img;
        this.jieshao = jieshao;
        this.price = price;
        this.number = number;
    }
}
